internal class MorseCodeDecoder
{
    public static string Decode(string morseCode)
    {
        return string.Join(" ",
            morseCode.Trim().Split("   ")
                .Select(word => string.Join("", word.Split(' ').Select(x => MorseCode.Get(x)))));
    }
}

